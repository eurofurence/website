<h1>Furauction</h1>

<section class="uk-column-1-2@l">

	<div>
		<h2>What is it?</h2>
		<p>During our Furauction, attendees will have the opportunity to bid for a one-on-one meetup with you! But don't worry, it won't take up your entire convention time. The Meetup is limited to an hour after the Event ends. Though if the conversation is flowing and you're both enjoying the moment, it can be extended for as long as you'd like.</p>
	</div>

	<div>
		<h2>What Winning Means:</h2>
		<p>The highest bidder secures a one-on-one meetup with the fursuiter being auctioned.</p>
		<p>This could include anything that is within the limitations and both are okay with. Things like meeting the suiter, casual conversation, hugs, or photoshoots could be great ideas.</p>
		<p>If the suiter feels unready or uncomfortable to grant the winning bidder full decision-making power, they may outline interactions that the winning bidder can choose from beforehand. This ensures a comfortable experience for both the suiter and the bidder.</p>
	</div>
	
	<div>
		<h2>Limitations:</h2>
		<ul>
			<li>Boundaries regarding the Nature of the meetup are set beforehand.</li>
			<li>The Suiter is also allowed to adjust these boundaries before the meetup, either tightening or loosening the restrictions to ensure that their personal boundaries are clarified.</li>
			<!-- <li>Legal and ethical guidelines to be followed strictly.</li> -->
			<li>No personal or invasive requests can be accommodated. This is an entirely SFW Meetup. This rule is a must and can't be adjusted by anyone.</li>
			<li>All interactions must be conducted in a professional and respectful manner.</li>
			<li>We recommend including a trusted handler, chosen by the fursuiter, to accompany und monitor interactions with the winning bidder. This ensures that nothing is forced upon the suiter und all interactions remain comfortable und consensual.</li>
			<li>Please be advised that you are not required to remain with the auctioneer for the entire duration if you feel unwell or any significant concerns or issues arise.</li>
		</ul>
	</div>
</section>

