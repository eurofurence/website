<section> 
	<div> 
		<h2>Volunteer Roles and Special Requirements</h2> 
			<p>Most Critters can sign up for shifts directly in the Critter System after joining the <strong>Allround Critter</strong> group. These roles cover a wide range of tasks, from helping with logistics to supporting attendees during the event. No special training is needed for most roles, so you can start volunteering right away.</p> 			
			
			<p>Some tasks require an on-site certification before you can sign up, for example any shift that involves our theater and dance stages. Certifications are provided through short safety briefings or training sessions. These will be clearly marked in the Critter System together with the dates and times. If you are interested in these tasks, please complete the certification before signing up so that everything runs safely and smoothly.</p>			
	</div> 
</section> 

<section>
	<div> 
		<h2>Shift Information</h2> 
			<p>Shifts are available throughout the convention and you can sign up for as many as you like. All shifts are coordinated through the Critter System, so make sure to log in regularly to see what is available and manage your schedule.</p>
	
			<p>Once you have signed up for a shift, please arrive on time and ready to contribute. If something unexpected comes up or you have questions, you can always talk to the Critter Team at the Eurofurence Info Desk who will be happy to help you.</p> 
			
			<div uk-lightbox>
					<a href="img/pages/volunteering/2025-08-19_crittersystem-shifts.jpg">
