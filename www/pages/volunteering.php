<section>
	<div>
		<h2>Requirements & Training</h2>
			<p>Most Critter tasks do not require any special qualifications. You can simply pick the shifts that match your time and energy..</p>
			
			<p>Some tasks, such as stage work or roles involving safety and health responsibilities, require a certification before you can join. Certifications are provided through short safety briefings or training sessions or have to be obtained before the convention. These will be clearly listed in the Critter System along with the dates and times. If you are interested in these tasks, please complete the certification first so that everything runs safely and smoothly. If a certification only has to be verified for a department, ask the Critter Team at the Eurofurence Info Desk for assistance.</p>
	</div>
</section>

<section>
	<div> 
		<h2>Critter Rewards</h2> 
			<p>Every hour you contribute as a Critter counts. To thank you for your time and energy, you can collect Critter Rewards based on your hours of volunteering. Rewards can be verified and picked up at the <strong>Eurofurence Info Desk</strong>.</p>

			<p>Examples of rewards include:</p>
		
			<ul>
				<li>The <strong>Volunteer Button</strong> after your first two hours</li>
				<li><strong>Priority Access Tickets</strong> for select events</li>
  				<li><strong>Stickers, bookmarks, postcards</strong> and other collectibles</li>
				<li>Entry into the <strong>Critter Art Raffle</strong> with the chance to win unique artwork</li>
			</ul>
				
			<p>The more hours you give, the more rewards you get. All items are limited.</p>
	</div> 
</section> 

<section>
	<div>
		<h2>Support and Information</h2>
			<p>The <strong>Critter Team</strong> is always there for you. Whenever you need help, have questions, or simply want to check in, come see us at the <strong>Eurofurence Info Desk in Foyer X</strong>.</p>
			
			<p>Whether it is about the Critter System, your shifts, or any other volunteering matter, our team is happy to assist you and glad to see you. The Info Desk is open daily from Wednesday to Saturday, 9:00 until midnight. Feel free to come by at any time!</p>
	</div>
</section>

