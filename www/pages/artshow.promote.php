<H1>
Promote my art! &ndash; What is this about?
</H1>
<section class="uk-column-1-2@l">
	<div>
		<P>
			EF Art Show strives to help you show off your art during Eurofurence. This year, we are planning to provide additional ways to make our attendees aware of your Art Show artwork, inside and outside of the the Art Show. If you want to present art of yours to a broader audience, this is your chance!
		</P>
		<P>
			<strong>&#9432;</strong> Note: This offer is only available for clean (non-nsfw) artwork.
		</P>
	</div>

	<div>
		<h2>How we will promote the art</h2>
		<p>
			We will show the images on screens in the Art Show itself and on public screens all around the venue. For example on a screen next to the Dealers Den sponsor queue. Depending on the screen location, it will be shown between other information like timetable, livestreams and alike.
		</p>
	</div>

	<div>
		<h2>What we will and won't do with the images you provide</h2>
		<p>
			We will not use the images for any purpose other than the promotion of your exhibit, only during the convention, and only visible for people within the convention space.
		</p>
		<p>
			The copyright, of course, stays yours; you give us only a non-exclusive, royalty-free license for the purposes listed here. We will only use the images on the convention this year.
		</p>
		<p>
			We will not create (physical) copies of any kind.
		</p>
		<p>
			All display is best-effort. We strive for technical perfection, but cannot guarantee perfect reproduction quality of the screens and projectors we are going to use.
		</p>
		<p>
			We reserve the right to reject promoting any picture for any reason. Such reasons might be poor image quality, an unbalance between certain topics, themes not suitable for a broad audience (esp.violence, fetishes, etc.), or pictures that do not represent the original exhibits in a meaningful way when shown on a screen. Please make sure you provide us with a file that reflects the quality of your exhibit, especially when scanning/photographing physical media. A bad photo taken with a mobile phone will not work in your favor.
		</p>
		<p>
			We will contact you and ask for your permission first if we come up with any ideas to promote your picture not covered by the above.
		</p>
	</div>

	<div>
		<h2>Specifications and tips for a great display</h2>

		<p>
			Best provide us with an image that has already the proper dimensions. When your image file does not meet these respective dimensions, we'll resize it for you.
			<ul>
				<li>Images in landscape format re-scale to width: 1500px</li>
				<li>Images in portrait format re-scale to height: 1080px</li>
			</ul>
		</p>	
		<p>
			Do not add a background, frame, logo, text, watermarks and alike, unless they are present on the physical piece as well. We will only show images that represent the real exhibit shown in the art show itself.
		</p>
		<p>
			We only accept JPGs. Use lowest compression possible for best quality. A file size up to 5Mb is allowed.
		</p>
