<section>
	<h2 class="uk-margin-remove-bottom">Illustrations</h2>
	<h3 class="uk-margin-remove-top">(If you're an artist, this one is for you!)</h3>
	<ul class="uk-column-1-2@l">
		<li>The Conbook is in full color, so we prefer color illustrations as well.</li>

		<li>We are looking for full page illustrations as well as smaller fillers.</li>

		<li>The format for full page illustrations is A4, upright (210 x 297mm) plus 3mm cropping area along all edges, adding up to a total of 216 x 303 mm or 2551 x 3578 pixels at 300 dpi.<br /><br />
			<img src="img/pages/conbook/How-to-conbook_1.jpg" alt="How to Conbook" />
		</li>
		<li>File format should be JPG set to highest quality (low information loss). Other formats are acceptable too but may cause issues due to the resulting file size.</li><br/>
		<li>For a full page illustration, do not draw anything important into the cropping area. Please avoid symmetrical motifs or motifs that rely on exact cutting of the page &ndash; for technical reasons, the cropping of the finished book may occur anywhere in the cropping area of each page.</li><br/>
		<li>Fillers that do not have an illustrated background (extracted cutouts) need to have a fully transparent image background, since the pages themselves will have their own color background. The foreground needs to be intransparent (opaque). Be careful about holes in the foreground (transparent) and white foreground elements (opaque)!
			<img src="img/pages/conbook/How-to-conbook_2.jpg" alt="How to Fillers" class="uk-margin uk-margin-top" /> 
		</li>

		<li>To submit illustrations that contain transparency, please use the PNG format.</li>

		<li>The submission must NOT exceed PG-13 rating.</li>

		<li>The artists for the cover art work and the centerfold will be chosen and approached directly by the team.</li>

		<li>Sometimes we can include horizontal pictures in the Conbook but usually these spots are not planned in advance. If you have something that fits the theme you can still send it in. So we might only be able to show a part of it or use it as a background image.</li>
	</ul>
	
	<hr />

	<div class="uk-column-1-2@l">
		<p>While there is no ongoing contest, there's still a challenge in getting into the book! Acceptance depends on quality, total number of images submitted, layout requirements, and the implementation of the con's topic! We will not be able to tell you immediately whether your submission makes the cut; this can be decided only in the final layout, so please be patient.</p>
		<p>If you have graphic ideas other than illustrations which fit with the topic, you're welcome to submit them as well!</p>
		<p>Even if your artwork does not get featured in the Conbook itself, we will always try to include it if applicable in various media across the convention (e.g. banners, dance visuals ect.)</p>
