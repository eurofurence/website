                                The host will start a countdown again after 30 seconds which indicates that the first
                                assigned dancer gets back to their spot and the second dancer is allowed to dance their 30
                                seconds.<br/>
                                Now the first dance battle is completed.
                            </p>

                            <p>
                                Subsequently starts the process of song-voting (if the previous round did not skip a song)
                                and dancing for the second round.<br/>
                                In each duel you will dance two times to two different songs.
                            </p>

                            <p>
                                Afterwards the jury votes who advances to the next round by sticking their hands to the
                                winning dancer.<br/>
