<p>The Eurofurence Pawpet Show aims to bring a spectacular puppet show to the stage, allowing the audience to emerge in our worlds and feel for our characters. Setting up each scene is an enormous tasks which we handle as a group, and we are looking for your help!</p>
<p>As a Technical Assistant you will be part of a group trusted in setting up each scene as needed during our show, in-between the scenes while the curtain is closed. The set pieces and props we use will long be ready by then, but your team will prepare and put on stage all set pieces, props and puppets according to a detailed plan that we will provide you with.</p>
<p>The scene change process is handled by multiple sub-teams, and depending on where you want to help, you’ll be trusted with different tasks:</p>

<ul>
    <li class="uk-margin">Puppet Handler<br />
        Organize our puppets behind the scenes and hand them out to our players.
    </li>
    <li class="uk-margin">Dresser<br />
        Change our puppets’ costumes as needed for the next scene.
    </li>
    <li class="uk-margin">Props Handler<br />
        Organize, hand-out and sort our various props (like swords, guns or hand-cuffs).
    </li>
    <li class="uk-margin">Set Pieces Handler<br />
        Organize and set-up set pieces on stage (like beds, trees or sails).
    </li>
    <li class="uk-margin">Special Effects Handler<br />
        Organize and set-up/hand-out special effects (like fog machines or telekinesis machines).
    </li>
</ul>

<p>The role of Technical Assistant is a purely technical one, so acting skills are not required and you will be behind the stage during the scenes. Participation in our off-site rehearsals before the convention is not required (but of course you are welcomed to, if you want)!</p>

<p>
    <strong>Skills Required</strong><br />
    You do not need any prior theater experience, nor do you need any acting skills!
</p>

Must haves:
<ul>
    <li>Ability to lift and carry somewhat large and sometimes somewhat heavy objects.</li>
    <li>Sufficient proficiency in the English language.</li>
    <li>Personal protection equipment (work shoes and gloves).</li>
</ul>

Other than that it’s good to have:
<ul>
    <li>Light organizational skills.</li>
    <li>German language proficiency.</li>
</ul>

<p>You can learn everything specific to our show and theater production in general on the job and we’d be happy to share our knowledge with you!</p>

<p>
    <strong>Your Time-Invest Before the Convention</strong><br />
    Generally, Technical Assistants are only active during Eurofurence on-site. As such, your only time-invest before the convention is one or two evenings/afternoons for an online-call in which we brief you about the upcoming show, our documents and the current show’s specialties.
</p>

<p>
    <strong>Your Time-Invest at the Convention</strong><br />
    You need to fully participate in our dress rehearsal (usually on Thursdays) and our live show (usually on Saturdays). Apart from that, two or three hours in our workshop before the dress rehearsal for a “hands-on”-introduction to how our scene-changes and stage-tech work, about half an hour for a security briefing, and an hour of your time during our tech rehearsal (usually on Tuesdays) to get familiar with our stage.
</p>
<p>This is all or nothing, however. To receive the experience and knowledge needed for these roles, you have to attend all on-site events as outlined above.</p>

