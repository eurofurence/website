<ul>
    <li><strong>Tech-Savvy:</strong> Comfortable using online systems and tools, with a basic understanding of how to manage shifts within a volunteer management system.</li>
    <li><strong>Convention Experience:</strong> Previous experience volunteering at or working for a convention, with an understanding of the flow and needs of such events.</li>
    <li><strong>Multilingual:</strong> Ability to communicate in German or other languages to assist a diverse group of volunteers.</li>
</ul>

