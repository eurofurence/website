<p>The Eurofurence Critter System is the backbone of our volunteer management, ensuring that the convention runs smoothly by coordinating shifts and tasks for our dedicated volunteers. As Europe’s largest furry convention, Eurofurence relies on this system to match volunteers with the areas that need their help the most. We are looking for enthusiastic and organized individuals to join our team as Critter System Info Desk Staff.</p>

<h3>Your Responsibilities</h3>

<p>As Critter System Info Desk Staff, you will play a crucial role in managing the volunteer experience during the convention. Your main duties will include: </p>

<ul>
    <li><strong>Volunteer Assistance:</strong> Help volunteers sign up for shifts using the Critter System, ensuring they understand how to navigate the system and select appropriate shifts.</li>
    <li><strong>Department Support:</strong> Assist various Eurofurence departments in creating, maintaining, and adjusting their shifts as needed.</li>
    <li><strong>On-Site Guidance:</strong> Provide on-site guidance to volunteers, directing them to areas that urgently need help and ensuring they know where to go and what to do.</li>
    <li><strong>Management Systems:</strong> Manage the Dealers' Den Management System for online dealer applications, assistant management, and on-site floor management.</li>
    <li><strong>Answering Questions:</strong> Serve as the go-to person for volunteers with questions about shifts, duties, or the Critter System itself, providing clear and helpful information.</li>
    <li><strong>Coordination with Other Departments:</strong> Monitor the Critter System to identify and fill critical gaps in volunteer coverage, ensuring all shifts are adequately staffed.</li>
    <li><strong>Shift Management:</strong> Efficiently manage the budget allocated for the Dealers' Den, ensuring all expenses are tracked and within limits.</li>
    <li><strong>General Support:</strong> Assist with other volunteer management tasks as needed, ensuring the smooth operation of the convention’s volunteer workforce.</li>
    </ul>

<h3>Your Profile</h3>

<p>You should have the following qualifications and qualities:</p>

<ul>
    <li><strong>Availablity:</strong>
        <ul>
            <li>Able to commit several hours a month throughout the year.</li>
             <li>20 hours on-site during Eurofurence</li>
            <li>Present on-site from early arrival until departure (Con-Day -1 to Con-Day 5)</li>
        </ul>
    </li>
    <li><strong>Communication Skills:</strong> Excellent communicator who is patient, approachable, and able to explain processes clearly to volunteers.</li>
    <li><strong>Problem-Solving:</strong> Quick thinker who can handle last-minute changes and direct volunteers to where they are most needed.</li>
     <li><strong>Customer Service:</strong> A friendly and helpful demeanor, with a focus on providing great support to volunteers and departments.</li>
    <li><strong>Language Skills: </strong> Fluent in written and spoken English. </li>
</ul>

<h3>Nice-to-Haves</h3>

