<p>The Eurofurence Pawpet Show aims to bring a spectacular puppet show to the stage, allowing the audience to emerge in our worlds and feel for our characters. Bringing our visions to life requires a lot of artwork, both in classic 2D form (traditional and/or digital) as well as 3D form. Creativity knows no limits, and thus we are looking for you!</p>

<p>Depending on what you want to do and what talents you bring, we have different tasks you can help us with:</p>
<ul>
    <li class="uk-margin">2D Artist<br />
        As a 2D artist you will be able to help create the graphics we use for our backgrounds and set pieces, as well as specific props as required.
    </li>

    <li class="uk-margin">3D Artist<br />
        As a 3D artist you will be able to help create our animated graphics and scenes for our backgrounds, the intro, and various other media we use for our show.
    </li>

    <li class="uk-margin">Concept Artist<br />
        As a concept artist you will work together with our script writers to create rough concepts that visualize our writers’ visions to help the team as whole to create all needed backgrounds, props set pieces and animations.
    </li>
</ul>

<p>These roles aren’t mutually exclusive, of course! If you want to bring 2D animations to the show and create concept art at the same time, for example, we would be very happy to have you!</p>

<p>
    <strong>Skills Required</strong><br />
    You do not need any prior theater experience, nor do you need any acting skills!
</p>

Must haves:
<ul>
    <li>Sufficient proficiency in the English language.</li>
    <li>Sufficient artistic skills, especially for non-living objects and scenes.</li>
</ul>

Other than that it’s good to have:
<ul>
    <li>German language proficiency.</li>
</ul>

<p>You can learn everything specific to our show and theater production in general on the job and we’d be happy to share our knowledge with you!</p>

<p>
    <strong>Your Time-Invest Before the Convention</strong><br />
    Production of all needed artwork usually starts at around March or April and lasts until July or August. Digital media production lasts until right before the con. As such, plan in enough time to finish what you’d like to take on during this time-frame.
</p>

<p>You do not need to take part in our rehearsals, but are still welcomed to, if you want to.</p>

<p>
    <strong>Your Time-Invest at the Convention</strong><br />
