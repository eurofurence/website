<ul>
    <li>Project Managing Skills</li>
    <li>Organizational Skills</li>
    <li>Sufficient proficiency in the English Language</li>
    <li>Ability to travel to our off-site rehearsals (Dortmund, Germany)</li>
</ul>

Other than that it’s good to have:
<ul>
