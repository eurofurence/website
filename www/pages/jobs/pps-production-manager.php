<p>The Eurofurence Pawpet Show aims to bring a spectacular puppet show to the stage, allowing the audience to emerge in our worlds and feel for our characters. Organizing this project is an exciting task and we are looking for help in doing so!</p>
<p>As a Production Manager or Assistant you will be part of a small team responsible for managing, guiding and evolving the overall production process of the Pawpet Show. This includes “classic” managing tasks such as coordination of our teams, time scheduling and project management, but also show-related tasks such as overseeing the rehearsals and backstage management.</p>
<p>With your help, we will be a team of three production managers, so you will not be alone! Depending on how much experience you have, you can either start out in a very active managing role right away or learn on the job as an assistant.</p>

<p>The tasks which we handle as a <span class="uk-text-italic">team</span> are:</p>
<ul>
    <li>Project Management</li>
    <li>Team Coordination and Internal Communication</li>
    <li>Coordination and Communication with other Eurofurence Departments</li>
    <li>Scheduling</li>
    <li>Personnel Planning</li>
    <li>Stage Management</li>
    <li>Documentation</li>
    <li>Budget Planning</li>
    <li>On-Site Workshop Coordination</li>
    <li>Extracting and overseeing concrete todo items from the show’s script.</li>
    <li>Resource Planning</li>
    <li>Procurement of Materials</li>
    <li>Managing of Outside Contracts (Rehearsal Location, Catering, ...)</li>
</ul>

<p>
    <strong>Skills required</strong><br />
    You do not need any prior theater experience, nor do you need any acting skills.
</p>

