<p>The Eurofurence Pawpet Show aims to bring a spectacular puppet show to the stage, allowing the audience to emerge in our worlds and feel for our characters. Each show is presented by a team of close to 30 people, bringing both puppets and props to life and telling our stories through them. We can always use more helping hands, and thus are looking for you!</p>
<p>Depending on what you want to do and what talents you bring, we have different tasks you can help us with:</p>
<ul>
    <li class="uk-margin">Puppeteer<br />
        As a puppeteer you will be able to play our characters themselves, either extra roles in the background or story driving roles with a speaking role.</li>
    <li class="uk-margin">Stage Hand<br />
    As a stage hand, you will play the various props and set pieces our characters interact with and/or help creating these props, set pieces and special effects.</li>
</ul>

<p>By filling one of these roles, you will be on stage during the scenes and directly help in bringing the story come to life. You won’t be visible (duck your head!), but your work will be.</p>

<p>
    <strong>Skills Required</strong><br />
    You do not need any prior theater experience, nor do you need any acting skills! We are happy to help you get into the groove during our rehearsals, but if you bring some experience in these fields we’d be very happy to have you.
</p>

Must haves:
<ul>
    <li>Sufficient proficiency in the English language.</li>
    <li>Ability to move around on a crowded stage.</li>
    <li>Ability to perform in stressful situations.</li>
</ul>

Other than that it’s good to have:
<ul>
    <li>German language proficiency.</li>
    <li>Previous (voice) acting skills.</li>
</ul>

<p>You can learn everything specific to our show and theater production in general on the job and we’d be happy to share our knowledge with you!</p>

<p>
    <strong>Your Time-Invest Before the Convention</strong><br />
    Generally, puppeteers and stage hands need to be able to take part in our four rehearsals during the months before Eurofurence takes place. One rehearsal lasts one full weekend and takes place in Dortmund, Germany.
</p>
<p>Apart from that, plan in some time for online-calls and to read the script as a way to prepare yourself for your role.</p>
<p>If you want to create props, set pieces or special effects, plan in some time to build them between the rehearsals. Participation in our building weekend, usually one or two weeks before Eurofurence, is also very much appreciated.</p>

<p>
    <strong>Your Time-Invest at the Convention</strong><br />
    You need to fully participate in our dress rehearsal (usually on Thursdays) and our live show (usually on Saturdays). Apart from that, a few hours of your time to help in our workshop to set up all props and set pieces is very much appreciated.
</p>

