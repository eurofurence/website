	<h2 id="ddglossaryterms">Glossary and Terms</h2>
	<div>
		<h3 class="reset-font">Dealer</h3>
		<p>The dealer is the single person ultimately responsible for the dealership and the authority for questions from staff. A dealer can only run one dealership. A dealer needs a valid Eurofurence event registration to purchase a dealership package and for the on-site Dealers’ Den Check-In.</p>
	</div>

	<div>
	<h3 class="reset-font">Dealer Assistant</h3>
		<p>Any person working in a dealership needs to be registered as a dealer assistant. A dealer assistant is supporting the dealer, while holding the same access and sales privileges as the respective dealer themself. Dealer assistant status is particularly important during setup and teardown, as only dealers and dealer assistants are granted access into the Dealers’ Den during those times. Dealerships may have multiple dealer assistants, based on the size of the dealership. Each dealer assistant can only be assigned to one dealership, while still being permitted to support other dealerships. Dealer assistants need a valid Eurofurence event registration for the on-site Dealers’ Den Check-In.</p>
	</div>

