	<h2 class="uk-column-span">The <i>Short</i> Version</h2>
	<p>Sales in the Dealers’ Den are only allowed by registered dealers in  authorized dealerships within designated spaces. Sharing space at a dealership with unauthorized dealers is not allowed.</p>
	<p>In order for your registration as a dealer or dealer assistant for the Eurofurence Dealers' Den to be taken into consideration, only a valid registration for the Eurofurence event via the Eurofurence <a href="https://identity.eurofurence.org" target="_blank">Registration System</a> itself is mandatory; payment for the ticket and Dealers' Den fees are handled after dealership approval. After registering for Eurofurence a dealership application can be submitted in the <a href="https://dealers.eurofurence.org" target="_blank">Dealers’ Den Registration System</a>.</p>
	<p><strong>Electricity is available for dealerships.</strong> We offer electricity for low-power consumption devices only.</p>
	<p>Dealerships are available in different (table) sizes. During Dealers’ Den registration, the desired size and table configuration can be applied for. Dealership space is limited, and the Dealers’ Den Management will try to accommodate as many artists and dealers as possible without cutting down or denying requested space upon availability. During placement, we will also try to consider requests in regard to location and dealership neighbors.</p>
	<p>Dealership applications will be queued into a curated system; the application will be reviewed by the Dealers’ Den Management, approved or declined, then placed. Payment for dealerships is not due until the dealer has confirmed their assigned dealership size. Placement in the room commences thereafter. The period for application will be approximately six weeks.</p>
	<p>Any dealerships not initially accepted will be placed on a waiting list. If a dealership space becomes re-available due to cancellation of another dealer, the next best match based on curation from the waiting list will be contacted. There might be last-minute sales of canceled dealership spaces at the convention itself.</p>
	<p>Once the registration phase for the Dealers' Den has closed, the curation phase will begin. The Dealers' Den team aims to complete the curation process within four weeks. Every applicant will receive a definite response: whether their application has been accepted, placed on the waiting list, or, if necessary, declined due to not meeting the selection criteria.</p>
	<p><strong>Dealership packages are non-refundable and cannot be canceled. However, transfers of dealership packages are possible but can only be initiated and approved by the Dealers' Den Management. Dealers cannot select or nominate individuals from the waiting list for a transfer to bypass the curation process.</strong> If a transfer is desired, please contact the Dealers' Den Management with your suggestion. All transfer requests will be considered by the management team, who will have the final say in the approval of the new dealer.</p>
	<p>Dealers will have setup slots on Wednesday evening and early Thursday to set up and prepare their dealerships. A convention badge is required for Dealers’ Den Check-In; for all dealers and their assistants.</p>
	<p>Items and services in violation of applicable rules or laws must not be offered. Adult material is only permissible on public display in the “After Dark” section of the Dealers’ Den. Dealership build-ups must not reach into other dealerships or walkways. Lights, music or sound, and strong smells or fumes must be kept to a minimum.</p>
	<p>The sale or offering of items that imitate third-party copyrighted material is prohibited. For further information, please refer to the full details down below.</p>
	<p><strong>Teardown begins on Saturday at 18:00. All dealerships must be cleared out, and dealers must check out with the Dealers’ Den Staff no later than 20:00. Each dealer is responsible for cleaning up their dealership area. Before leaving the Dealers' Den, a checkout with a Dealers’ Den staff member is required. Please visit the help desk to complete your checkout process before departing.</strong></p>
	<p>Dealers’ Den Staff have a say on all matters. The Dealers’ Den Management’s decisions are final.</p>
	<p>Any person requesting a dealership at Eurofurence is required to agree to and accept the “Dealers’ Den Rules” set in this document.</p>
	<p>If you have any questions after reading the <span class="uk-text-italic">Dealers’ Den Rules & Information</span> and the FAQ, please feel free to email us at dealers@eurofurence.org.</p>
</section>

